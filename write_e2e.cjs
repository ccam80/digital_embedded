a('// Shared helper: open property popup for a labeled element by double-click');
a('// ---------------------------------------------------------------------------');
a('');
a('async function openPopupForLabel(builder: UICircuitBuilder, label: string): Promise<void> {');
a('  const info = await builder.getCircuitInfo();');
a('  const el = info.elements.find(e => e.label === label);');
a('  expect(el, `Element "${label}" not found`).toBeTruthy();');
a('  const coords = await builder.toPageCoords(el!.center.screenX, el!.center.screenY);');
a('  await builder.page.mouse.dblclick(coords.x, coords.y);');
a("  await expect(builder.page.locator('.prop-popup')).toBeVisible({ timeout: 3000 });");
a('}');
a('');
a('/**');
a(' * Set a model parameter value in the property popup by finding the row whose');
a(' * label span exactly matches paramKey and filling the adjacent input.');
a(' * Works for both primary params (visible immediately) and secondary params');
a(' * (inside the Advanced Parameters section -- expand first if needed).');
a(' */');
a('async function setModelParamInPopup(');
a('  page: Page,');
a('  paramKey: string,');
a('  value: string | number,');
a('): Promise<void> {');
a("  const popup = page.locator('.prop-popup');");
a('  const inputHandle = await popup.evaluateHandle((popupEl, key) => {');
a('    const spans = popupEl.querySelectorAll("label, span");');
a('    for (const span of spans) {');
a('      if (span.textContent?.trim() === key) {');
a('        const row = span.parentElement;');
a('        if (row) {');
a('          const input = row.querySelector("input");');
a('          if (input) return input;');
a('        }');
a('      }');
a('    }');
a('    return null;');
a('  }, paramKey);');
a('  const input = inputHandle.asElement();');
a('  expect(input, `Model param "${paramKey}" input not found`).not.toBeNull();');
a('  await input!.fill(String(value));');
a("  await input!.press('Tab');");
a('}');
a('');
a('/**');
