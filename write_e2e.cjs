a(' * Find the input for a pin electrical field by its display label (e.g. Rout).');
a(' */');
a('async function getPinFieldInput(page: Page, fieldLabel: string) {');
a("  const popup = page.locator('.prop-popup');");
a('  const rows = popup.locator("div").filter({');
a('    has: page.locator(`span:text-is("${fieldLabel}")`),');
a('  });');
a('  return rows.locator("input").first();');
a('}');
