a('');
a('    // Mixed circuit: And gate A1 (inputs HIGH) -> RL (1kOhm) -> GND, Probe P1 at junction');
a('    // rOut is source impedance: V_junction = Voh * RL / (rOut + RL)');
a('    // Large rOut => small V_junction');
a("    await builder.placeLabeled('And', 4, 8, 'A1');");
a("    await builder.placeLabeled('Resistor', 12, 8, 'RL');");
a("    await builder.placeComponent('Ground', 14, 14);");
a("    await builder.placeLabeled('Probe', 18, 8, 'P1');");
a("    await builder.placeLabeled('In', 1, 6, 'Inp1');");
a("    await builder.placeLabeled('In', 1, 10, 'Inp2');");
a("    await builder.setComponentProperty('Inp1', 'Default', 1);");
a("    await builder.setComponentProperty('Inp2', 'Default', 1);");
a("    await builder.setComponentProperty('RL', 'resistance', 1000);");
a('');
a("    await builder.drawWire('Inp1', 'out', 'A1', 'In_1');");
a("    await builder.drawWire('Inp2', 'out', 'A1', 'In_2');");
