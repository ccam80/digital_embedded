a('');
a('/**');
a(' * Build a mixed digital-to-analog circuit using already-loaded builder.');
a(' * And gate A1 (both inputs HIGH) -> RL (1kOhm) -> GND, Probe P1 on junction.');
a(' * Returns page-absolute midpoint of the wire between A1.out and RL.A.');
a(' */');
a('async function buildMixedCircuitAndGetWireMid(');
a('  builder: UICircuitBuilder,');
a('): Promise<{ wireMidX: number; wireMidY: number }> {');
a("  await builder.placeLabeled('And', 4, 8, 'A1');");
a("  await builder.placeLabeled('Resistor', 12, 8, 'RL');");
a("  await builder.placeComponent('Ground', 14, 14);");
a("  await builder.placeLabeled('Probe', 18, 8, 'P1');");
a("  await builder.placeLabeled('In', 1, 6, 'Inp1');");
a("  await builder.placeLabeled('In', 1, 10, 'Inp2');");
a("  await builder.setComponentProperty('Inp1', 'Default', 1);");
a("  await builder.setComponentProperty('Inp2', 'Default', 1);");
a("  await builder.setComponentProperty('RL', 'resistance', 1000);");
a('');
a("  await builder.drawWire('Inp1', 'out', 'A1', 'In_1');");
a("  await builder.drawWire('Inp2', 'out', 'A1', 'In_2');");
a("  await builder.drawWire('A1', 'out', 'RL', 'A');")
a("  await builder.drawWire('RL', 'A', 'P1', 'in');");
a("  await builder.drawWireFromPin('RL', 'B', 14, 14);");
a('');
a("  const fromPos = await builder.getPinPagePosition('A1', 'out');");
a("  const toPos = await builder.getPinPagePosition('RL', 'A');");
a('  const wireMidX = Math.round((fromPos.x + toPos.x) / 2);');
a('  const wireMidY = Math.round((fromPos.y + toPos.y) / 2);');
a('');
a('  return { wireMidX, wireMidY };');
a('}');
