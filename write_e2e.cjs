a("test.describe('Hot-loading model params via property popup', () => {");
a("  test('changing BF on BJT via primary param row changes output voltage', async ({ page }) => {");
a('    const builder = new UICircuitBuilder(page);');
a('    await builder.load();');
a('');
a('    // BJT common-emitter: Vcc(12V)->Rc(10k)->Q1:C, Ib(1mA)->Q1:B,');
a('    // Q1:E->GND, Probe Pc on collector');
a('    // With BF=100 (default): Ic = BF*Ib = 100*1mA = 100mA but Rc limits it');
a('    // With BF=10: Ic = 10*1mA = 10mA, Vc = 12 - 10mA*10k = 2V');
a('    // With BF=100: Ic = 100*1mA = 100mA (saturated), Vc near 0V');
a('    // So changing BF changes the output voltage significantly');
a("    await builder.placeLabeled('DcVoltageSource', 3, 5, 'Vcc');");
a("    await builder.placeLabeled('CurrentSource', 3, 12, 'Ib');");
a("    await builder.placeLabeled('Resistor', 10, 5, 'Rc');");
a("    await builder.placeLabeled('NpnBJT', 16, 9, 'Q1');");
a("    await builder.placeComponent('Ground', 6, 16);");
a("    await builder.placeComponent('Ground', 18, 16);");
a("    await builder.placeComponent('Ground', 6, 9);");
a("    await builder.placeLabeled('Probe', 22, 7, 'Pc');");
a('');
a("    await builder.setComponentProperty('Vcc', 'voltage', 12);");
a("    await builder.setComponentProperty('Ib', 'Current (A)', 0.001);");
a("    await builder.setComponentProperty('Rc', 'resistance', 10000);");
a('');
a("    await builder.drawWire('Vcc', 'pos', 'Rc', 'A');");
a("    await builder.drawWire('Rc', 'B', 'Q1', 'C');");
a("    await builder.drawWire('Ib', 'pos', 'Q1', 'B');");
a("    await builder.drawWireFromPin('Q1', 'E', 18, 16);");
a("    await builder.drawWireFromPin('Vcc', 'neg', 6, 16);");
a("    await builder.drawWireFromPin('Ib', 'neg', 6, 9);");
a("    await builder.drawWire('Rc', 'B', 'Pc', 'in');");
a('');
a('    // Baseline step with DEFAULT BF=100');
a('    await builder.stepViaUI();');
a('    await builder.verifyNoErrors();');
a('    const defaultState = await builder.stepAndReadAnalog(200);');
a('    expect(defaultState).not.toBeNull();');
a("    const vcDefault = defaultState!.nodeVoltages['Pc'];");
a('    expect(vcDefault).toBeDefined();');
a('');
a('    // Open popup and change BF from 100 to 10 via the primary param row');
a("    await openPopupForLabel(builder, 'Q1');");
a("    await setModelParamInPopup(page, 'BF', 10);");
a('    await page.keyboard.press("Escape");');
a('');
a('    // Step again to pick up hot-loaded BF change');
a('    const hotState = await builder.stepAndReadAnalog(200);');
a('    expect(hotState).not.toBeNull();');
a("    const vcHot = hotState!.nodeVoltages['Pc'];");
a('    expect(vcHot).toBeDefined();');
a('');
a('    // Collector voltage must differ between BF=100 and BF=10');
a('    // At least 0.5V difference expected (likely much more)');
a('    expect(Math.abs(vcHot! - vcDefault!)).toBeGreaterThan(0.5);');
a('  });');
a('});');
